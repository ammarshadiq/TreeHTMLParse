    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        // present the previous text string for the return value
        replaceTextEdit = replaceTextBackup;
        addReplacedStringView.setVisible(false);
        
        //close this window frame
        addReplacedStringView.dispose();
    }//GEN-LAST:event_cancelButtonActionPerformed

    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // present the current text string for the return value
        this.containingTextField.getText();
        this.replaceWithTextField.getText();
        replaceTextEdit = new String[]{this.containingTextField.getText(), this.replaceWithTextField.getText()};
        replaceTextBackup = null;

        //close this window frame
        addReplacedStringView.setVisible(false);
        addReplacedStringView.dispose();
    }//GEN-LAST:event_okButtonActionPerformed

