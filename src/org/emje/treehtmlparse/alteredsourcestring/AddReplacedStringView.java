    public AddReplacedStringView(java.awt.Frame parent, boolean modal,  String title, String containingText, String replaceWith) {
        super(parent, modal);

        initComponents();
        setTitle(title);

        if (containingText != null && replaceWith != null){
            replaceTextBackup = new String[]{containingText, replaceWith};
            this.containingTextField.setText(containingText);
            this.replaceWithTextField.setText(replaceWith);
        }
    }

    /** edit command */
    public static String[] showDialog(java.awt.Frame parent, boolean modal, String title, String containingText, String replaceWith){
        addReplacedStringView = new AddReplacedStringView (parent, modal, title, containingText, replaceWith);
        addReplacedStringView.setLocationRelativeTo(parent);
        addReplacedStringView.setVisible(true);
        return replaceTextEdit;
    }

    /** add command */
